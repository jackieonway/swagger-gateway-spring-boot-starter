package com.github.jackieonway.swagger.entity;


/**
 * @author Jackie
 */
public class CloudRoute {

    /**
     * gateway route id
     */
    private String id;

    /**
     * route name
     */
    private String name;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
